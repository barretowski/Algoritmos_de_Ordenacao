package Listas;

public class Lista {
    private No inicio;
    private No fim;

    public void setInicio(No inicio){
        this.inicio = inicio;
    }

    public No getInicio(){
        return inicio;
    }

    public void setFim(No fim){
        this.fim = fim;
    }

    public No getFim(){
        return fim;
    }
}
